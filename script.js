window.onload = function() {
  if(!localStorage.getItem('movieKey0')) {
    populateStorage();
  }  
  else {
    setStyles();
  }
}


function setStyles() {
  for (i = 0; i < 121; i++){
    if (localStorage.getItem('movieKey' + i) == 'notPicked') {
      document.getElementById('movieKey' + i).className = "col notPicked";}
    else if (localStorage.getItem('movieKey' + i) == 'picked') {
    document.getElementById('movieKey' + i).className = "col picked";}
    else if (localStorage.getItem('movieKey' + i) == 'locked') {
      document.getElementById('movieKey' + i).className = "col locked";}
  }
}


function populateStorage() {
  for (i = 0; i < 121; i++){
    localStorage.setItem("movieKey" + i, 'notPicked')
  }
}


function clickedMovie() {
  var unPickedElements = $(this).siblings("div");
  var unPickedKeys = [];
  $(this).siblings().each(
    function (idx, el) {
    unPickedKeys.push(el.id);
  });
  function changeStyles(x) {
    for (i=0; i < unPickedElements.length; i++) {
      if (localStorage.getItem(unPickedKeys[i]) == 'locked') {
      return;
    }
  }
    if (localStorage.getItem(x.id) == "locked") {
      return;
    }
    else if (x.className == "col notPicked") {
      x.className = "col picked"
      localStorage.setItem(x.id, 'picked')
      for (i=0; i < unPickedElements.length; i++) {
        unPickedElements[i].className = "col notPicked";
      }
      for (i=0; i < unPickedElements.length; i++) {
        localStorage.setItem(unPickedKeys[i], 'notPicked');
      }
    }
    else {
      x.className = "col notPicked";
      localStorage.setItem(x.id, 'notPicked');
    }
  };
  changeStyles(this);
}


function lockInFunction() {
  var r = confirm("Are you sure you want to lock in your current picks?");
  if (r == true) {
  for (i = 0; i < 121; i++)
   if (localStorage.getItem("movieKey" + i) == "picked")
   localStorage.setItem("movieKey" + i, 'locked');
   setStyles();
  }
}

function resetPicksFunction() {
  var r = confirm("Are you sure you want to reset all of your picks?")
  if (r == true) {
  for (i = 0; i < 121; i++) {
  localStorage.setItem("movieKey" + i, 'notPicked')}};
  setStyles();
}














